//Asigancion basica del operador = 
var x = 5;
console.log("El valor de X es:", x);

//Se actualiza el valor de X.
x = x + 1;
console.log("El valor de X es:", x)

//Asignacion +=
x += 2;
console.log("El valor de X es:", x)

//Asignacion -=
x -= 2;
console.log("El valor de X es:", x)

//Asignacion *=
x *= 2;
console.log("El valor de X es:", x)

//Asignacion /=
x /= 2;
console.log("El valor de X es:", x)

//Asignacion %=
x %= 2;
console.log("El valor de X es:", x)
